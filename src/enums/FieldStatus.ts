/**
 * @enum FieldStatus - The status of a field.
 */
export enum FieldStatus {
  /**
   * @constant Enabled - The field is enabled.
   * @type {string}
   */
  Enabled = 'Enabled',

  /**
   * @constant Disabled - The field is disabled.
   * @type {string}
   */
  Disabled = 'Disabled',

  /**
   * @constant Invalid - The field is invalid.
   * @type {string}
   */
  Invalid = 'Invalid',
}
