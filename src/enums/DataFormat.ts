/**
 * @enum DataFormat - The format of the data to be returned.
 */
export enum DataFormat {
  /**
   * @constant Raw - The raw data will be returned.
   * @type {string}
   */
  Raw = 'Raw',

  /**
   * @constant Formatted - The formatted data will be returned.
   * @type {string}
   */
  Formatted = 'Formatted',
}
